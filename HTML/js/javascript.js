//자바스크립트의 가장 기본적인 연산(계산)
let students = 20;
//students = students + 1;
students += 1; //간략한 표현(복합대입연산자?)
console.log(students);
//students = 21

//students = students -1;
students -= 1;
console.log(students);
//studnets = 20;

//students = students * 2;
students *= 2;
console.log(students);
//students = 40;

//students = students / 2;
students /= 2;
console.log(students);
//students = 20;

//% 나머지 연산자 (나누고 나서의 나머지)
let extra = students % 3; // 20 / 3에 대한 나머지 값 즉, 2
console.log(extra);


